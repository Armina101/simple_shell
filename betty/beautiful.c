#include <stdio.h>

/**
* main - Entry point
* Description: This function calculates the sum of two integers and prints the result to the console
* Return: Always 0 (Success)
*/
#include <stdio.h>

int main(void)
{
int x = 10;
int y = 20;
int sum = x + y;

printf("The sum of %d and %d is %d\n", x, y, sum);

return (0);
}
